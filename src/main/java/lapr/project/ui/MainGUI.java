    private void addVehiclesFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVehiclesFileActionPerformed
        AddVehicleController addVechicleController = new AddVehicleController(project);
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            try {
                boolean result = addVechicleController.importVehiclesData(filePath);
                if (result) {
                    JOptionPane.showMessageDialog(null, "Vehicles added.");
                } else {
                    JOptionPane.showMessageDialog(null, "No new vehicles added.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Reading File", null, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_addVehiclesFileActionPerformed

