    private void roadConfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roadConfigActionPerformed
        AddRoadsController addRoadsController = new AddRoadsController(project);
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            try {
                boolean result = addRoadsController.importRoadsData(filePath);
                if (result) {
                    JOptionPane.showMessageDialog(null, "Roads added.");
                } else {
                    JOptionPane.showMessageDialog(null, "No new roads added.");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Reading File", null, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_roadConfigActionPerformed

